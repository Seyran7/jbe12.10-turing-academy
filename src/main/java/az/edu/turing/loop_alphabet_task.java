package az.edu.turing;

public class loop_alphabet_task {
    public static void main(String[] args) {

            System.out.println("Böyük hərflər:");
            for (char ch = 'A'; ch <= 'Z'; ch++) {
                System.out.print(ch + " ");
            }

            System.out.println("\n");

            System.out.println("Kiçik hərflər:");
            for (char ch = 'a'; ch <= 'z'; ch++) {
                System.out.print(ch + " ");
            }
        }
    }




